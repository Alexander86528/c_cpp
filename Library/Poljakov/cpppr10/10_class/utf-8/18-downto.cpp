#include <iostream>
using namespace std;
main()
{
  int n, k, M;     
  cout << "Введите число шагов цикла: ";
  cin >> M;
  n = 1;
  for ( k=1; k<=M; k++ ) 
    n = n * 2;
  for ( k=M; k>=1; k-- ) {
    cout <<  n << " ";
    n = n / 2;
    }
  cin.get(); cin.get();
}
